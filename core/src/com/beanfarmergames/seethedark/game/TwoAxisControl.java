package com.beanfarmergames.seethedark.game;

public class TwoAxisControl {
    float x = 0;
    float y = 0;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
