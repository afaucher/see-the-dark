package com.beanfarmergames.seethedark.components.configuration;

public class WeaponConfiguration extends ComponentConfiguration {
	private float weaponRange;
	private float weaponArcRad;
	private float weaponHeatPerJoule;
	private float weaponEmissionPower;
	
	public float getWeaponRange() {
		return weaponRange;
	}
	public void setWeaponRange(float weaponRange) {
		this.weaponRange = weaponRange;
	}
	public float getWeaponArcRad() {
		return weaponArcRad;
	}
	public void setWeaponArcRad(float weaponArcRad) {
		this.weaponArcRad = weaponArcRad;
	}
	public float getWeaponHeatPerJoule() {
		return weaponHeatPerJoule;
	}
	public void setWeaponHeatPerJoule(float weaponHeatPerJoule) {
		this.weaponHeatPerJoule = weaponHeatPerJoule;
	}
	public float getWeaponEmissionPower() {
		return weaponEmissionPower;
	}
	public void setWeaponEmissionPower(float weaponEmissionPower) {
		this.weaponEmissionPower = weaponEmissionPower;
	}
	
	
}
